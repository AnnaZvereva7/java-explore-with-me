
DROP TABLE IF EXISTS compilations_events;
DROP TABLE IF EXISTS compilations;
DROP TABLE IF EXISTS requests;
DROP TABLE IF EXISTS events;
DROP TABLE IF EXISTS categories;
DROP TABLE IF EXISTS users;

CREATE TABLE IF NOT EXISTS users (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
name VARCHAR(250) NOT NULL,
email VARCHAR(254) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS categories (
id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
name VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS events (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
title VARCHAR(120) NOT NULL,
annotation VARCHAR(2000) NOT NULL,
description VARCHAR(7000) NOT NULL,
category_id INT REFERENCES categories (id) ON DELETE RESTRICT NOT NULL,
event_date TIMESTAMP NOT NULL,
lon FLOAT NOT NULL,
lat FLOAT NOT NULL,
paid BOOLEAN NOT NULL,
request_moderation BOOLEAN NOT NULL,
participant_limit INT NOT NULL,
state VARCHAR(10) NOT NULL,
initiator_id BIGINT REFERENCES users (id) ON DELETE CASCADE NOT NULL,
created_on TIMESTAMP NOT NULL,
published_on TIMESTAMP,
admin_comment VARCHAR(7000)
);

CREATE TABLE IF NOT EXISTS requests (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
event_id BIGINT REFERENCES events (id) ON DELETE CASCADE NOT NULL,
requester_id BIGINT REFERENCES users (id) ON DELETE CASCADE NOT NULL,
created TIMESTAMP NOT NULL,
status VARCHAR(10) NOT NULL,
CONSTRAINT event_user UNIQUE (event_id, requester_id)
);

CREATE TABLE IF NOT EXISTS compilations (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
title VARCHAR(50) UNIQUE NOT NULL,
pinned BOOLEAN NOT NULL
);

CREATE TABLE IF NOT EXISTS compilations_events (
compilation_id BIGINT REFERENCES compilations (id) ON DELETE CASCADE,
event_id BIGINT REFERENCES events (id) ON DELETE CASCADE,
CONSTRAINT composite_key PRIMARY KEY(compilation_id, event_id)
)